        <p>
            Abstract. This article deals with the development of a system that catalogs indigenous words and their respective synonymous in Portuguese. The qualitative methodology was used based on a descriptive discussion to reflect the use of technology in language preservation. With technological appeal filed the user can learn the indigenous language as the catalogs. As a result indigenous words before passed only verbally in the villages will be available in text format, picture and sound. It concludes that the system will allow the indigenous vocabulary storage contributing to the preservation and transmission of these languages , acting as a reference material and use in indigenous literacy activities.
        </p>
        <p>
            Keywords: Indian words, indigenous vocabulary, indigenous, Xakriabá, information systems.
        </p>
        <p>
            Resumo. Este artigo trata do desenvolvimento de um sistema que cataloga palavras indígenas e seu respectivo sinônimo em português. A metodologia qualitativa foi utilizada tendo como base uma discussão descritiva de modo a refletir o uso da tecnologia na preservação linguística. Com o recurso tecnológico proposto o usuário poderá aprender a língua indígena à medida que a cataloga. Como resultado as palavras indígenas antes repassadas apenas verbalmente nas aldeias estarão disponíveis em formato de texto, imagem e som. Conclui-se que o sistema permitirá o armazenamento do vocabulário indígena contribuindo para a preservação e a transmissão dessas línguas, funcionando também como um material de consulta e utilização em atividades de alfabetização indígena.
        </p>
        <p>
            Palavras-chave: palavras indígenas, vocabulário indígena, indígenas, Xakriabá, sistemas de informação.
        </p>
